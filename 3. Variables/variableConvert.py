# Changing one variable type to another
x = '5'
print('x is a variable of type:')
print(type(x))

y = int(x) # convert x to an Integer called y
print('y is a variable of type:')
print(type(y))

z = 5.1
print('z is a variable of type:')
print(type(z))

w = 10/3
print('w is a variable of type:')
print(type(w))

p = 7
q = float(p)
print('q is a variable of type:')
print(type(q))
print('q =',q)

