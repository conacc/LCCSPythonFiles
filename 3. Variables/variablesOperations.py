# Use the different maths operations

# Define two variables
num1 = 3
num2 = 2
# adding
answer = num1 + num2
print(answer)
# subtraction
answer = num1 - num2
print(answer)
# multiplying
answer = num1 * num2
print(answer)
# normal division
answer = num1 / num2
print(answer)
# floor division
answer = num1 // num2
print(answer)
# powers or indices
answer = num1 ** num2
print(answer)
answer = num2 ** num1
print(answer)
# remainder
answer = num1 % num2
print(answer)
answer = num2 % num1
print(answer)



